# Polymorphism with class methods
class Kenya():
    def capital(self):
        print("The capital City of Kenya is Nairobi ")

    def language(self):
        print("Swahili is the most spoken language of Kenya")


class USA():
    def capital(self):
        print("Washington D.C is the capital city of USA")

    def language(self):
        print("The most spoken language is English")



